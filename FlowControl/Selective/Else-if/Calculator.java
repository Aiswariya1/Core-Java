public class Calculator {
    public static void main(String[] args) {

        int x = 10;
        int y = 20;
        char op = '+';

        if(op == '7'){
            System.out.println("Sum is" + (x+y));
        }
            else if(op == '-'){
                System.out.println("Sub is" + (x-y));
            }

            else if(op == '*'){
                System.out.println("Prod is" + (x*y));
            }

            else{
                 System.out.println("Invalid op");
           }

         }
     }


    
    

